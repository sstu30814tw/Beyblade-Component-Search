export const allParts: { name: string; nameCn?: string; type: Part["type"]; products: string[] }[] = (() => {
  const map = new Map<string, { name: string; nameCn?: string; type: Part["type"]; products: string[] }>();
  for (const product of products) {
    for (const part of product.parts) {
      const key = `${part.type}|${part.name}`;
      if (!map.has(key)) {
        map.set(key, { name: part.name, nameCn: part.nameCn, type: part.type, products: [] });
      }
      if (!map.get(key)!.products.includes(product.code)) {
        map.get(key)!.products.push(product.code);
      }
    }
  }
  return Array.from(map.values()).sort((a, b) => {
    if (a.type !== b.type) {
      const order = ["戰刃", "固鎖", "軸心", "輔助戰刃"];
      return order.indexOf(a.type) - order.indexOf(b.type);
    }
    return a.name.localeCompare(b.name);
  });
})();
